$(document).ready(function(){
    $(".rollover1").css({'opacity':'0'});
	$('#marcas_cen a').hover(
		function() {
			$(this).find('.rollover1').stop().fadeTo(500, 1);
		},
		function() {
			$(this).find('.rollover1').stop().fadeTo(500, 0);
		}
	)
});

